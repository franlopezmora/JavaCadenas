public class CaracteresEspeciales {
    public static void main(String[] args) {
        String cadena = "Hola \n Mundo";
        System.out.println("cadena = " + cadena);

        String cadena2 = "Hola \t Mundo";
        System.out.println("cadena = " + cadena2);

        String cadedena3 = "Hola \' Mundo";
        System.out.println("cadedena3 = " + cadedena3);

        String cadena4 = "Hola \"\" Mundo";
        System.out.println("cadena4 = " + cadena4);

        String cadena5 = "Hola \\ Mundo";
        System.out.println("cadena5 = " + cadena5);

    }
}
